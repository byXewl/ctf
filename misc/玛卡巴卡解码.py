def decode_text(encoded_text, encoding_rules):
    decoded_text = ''
    i = 0
    while i < len(encoded_text):
        found = False
        for letter, code in encoding_rules.items():
            if encoded_text[i:i+len(code)] == code:
                decoded_text += letter
                i += len(code)
                found = True
                break
        if not found:
            # 如果无法解码，则保留原编码
            decoded_text += encoded_text[i]
            i += 1
    return decoded_text

